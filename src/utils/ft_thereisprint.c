#include "../include/minishell.h"

int	ft_thereisprint(char *str)
{
	int	i;

	i = 0;
	while (str && str[i])
	{
		if (str[i] > 32 && str[i] < 127)
			return (1);
		i++;
	}
	return (0);
}
